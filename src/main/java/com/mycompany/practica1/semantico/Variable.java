package com.mycompany.practica1.semantico;


public class Variable {
    int Valor;
    String Nombre;

    public Variable(int Valor, String Nombre) {
        this.Valor = Valor;
        this.Nombre = Nombre;
    }

    
    public int getValor() {
        return Valor;
    }

    public String getNombre() {
        return Nombre;
    }
    
    
    
}
